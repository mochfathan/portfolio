/* ── EMAIL DRAFT ─────────────────────────────────────────── */

async function buildEmailDraft(lead, analysis) {
  const sourceNote = fields.sourceCitations.checked
    ? "\n\nSource notes: Lead intake form, approved offer text, and project brand voice were used. No external facts were added."
    : "";
  const approvalNote = fields.humanReview.checked
    ? "\n\nStatus: DRAFT FOR HUMAN REVIEW. Do not send until the account owner checks fit, claims, and next-step accuracy."
    : "\n\nStatus: REVIEW WARNING. This tool recommends approval before sending.";

  const aiResult = await callAI(
    `You are a business development writer. Write a concise, professional outreach email using the details below.
Return ONLY the email text — no subject line prefix, no explanation, no markdown.

Company: ${lead.company}
Contact name: ${lead.contact}
Industry: ${lead.industry}
Their goal: ${lead.goal}
Our approved offer: ${lead.offer}
Brand voice to match: ${lead.brandVoice}
Qualification summary: ${analysis.fit}; urgency: ${analysis.urgency}; recommended next step: ${analysis.nextAction}

Format: Subject line on first line, blank line, then email body. End with "Best,\nMarketing team".`
  );

  if (aiResult) {
    return aiResult + sourceNote + approvalNote;
  }

  // Simulation fallback
  return `Subject: Next step for ${lead.company}'s growth diagnostic

Hi ${lead.contact},

Thank you for sharing the context around ${lead.company}'s ${lead.industry.toLowerCase()} marketing goals. Based on your note, the highest-value starting point appears to be a focused diagnostic of campaign spend, landing-page conversion, and lead quality.

Recommended next step:
Schedule a 30-minute discovery call so we can review the current funnel, identify where performance is leaking, and decide whether a 30-day campaign sprint is appropriate.

Why this makes sense:
- Your stated goal indicates a measurable acquisition problem.
- The approved offer matches the need: ${lead.offer}
- Initial qualification: ${analysis.fit}; urgency: ${analysis.urgency}.

Best,
Marketing team${sourceNote}${approvalNote}`;
}

