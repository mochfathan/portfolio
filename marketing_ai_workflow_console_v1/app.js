/* ── UNIVERSAL AI CALLER ─────────────────────────────────── */
/*
  callAI(prompt) → Promise<string>

  Normalises the request/response for each provider so the
  rest of the app never needs to know which API is running.

  Provider     Endpoint                                      Auth header
  ──────────── ───────────────────────────────────────────── ────────────────────
  claude       https://api.anthropic.com/v1/messages         x-api-key + anthropic-version
  openai       https://api.openai.com/v1/chat/completions    Authorization: Bearer
  openrouter    https://api.openrouter.ai/v1/chat/completions Authorization: Bearer
  custom       <user-supplied base URL>/chat/completions     Authorization: Bearer
  simulation   (local JS — no fetch)                        —
*/

async function callAI(prompt) {
  const provider = fields.aiProvider.value;
  const model    = fields.aiModel.value;
  const apiKey   = fields.apiKey.value.trim();

  if (provider === "simulation") {
    return null; // signal to caller: use local fallback
  }

  if (!apiKey) {
    throw new Error("API key is required. Enter your key in Step 0.");
  }

  let url, headers, body;

  if (provider === "claude") {
    url = "https://api.anthropic.com/v1/messages";
    headers = {
      "Content-Type":      "application/json",
      "x-api-key":         apiKey,
      "anthropic-version": "2023-06-01",
      "anthropic-dangerous-direct-browser-access": "true",
    };
    body = JSON.stringify({
      model,
      max_tokens: 1024,
      messages: [{ role: "user", content: prompt }],
    });

  } else if (provider === "openai") {
    url = "https://api.openai.com/v1/chat/completions";
    headers = {
      "Content-Type":  "application/json",
      "Authorization": `Bearer ${apiKey}`,
    };
    body = JSON.stringify({
      model,
      max_tokens: 1024,
      messages: [{ role: "user", content: prompt }],
    });

      } else if (provider === "openrouter") {
    url = "https://openrouter.ai/api/v1/messages";
    headers = {
      "Content-Type":  "application/json",
      "Authorization": `Bearer ${apiKey}`,
    };
    body = JSON.stringify({
      model,
      max_tokens: 1024,
      messages: [{ role: "user", content: prompt }],
    });

  } else if (provider === "custom") {
    const base = fields.customEndpoint.value.trim().replace(/\/$/, "");
    if (!base) throw new Error("Enter your custom endpoint URL in Step 0.");
    url = `${base}/chat/completions`;
    headers = {
      "Content-Type":  "application/json",
      "Authorization": `Bearer ${apiKey}`,
    };
    body = JSON.stringify({
      model: model === "default" ? undefined : model,
      max_tokens: 1024,
      messages: [{ role: "user", content: prompt }],
    });

  }

  const response = await fetch(url, { method: "POST", headers, body });

  if (!response.ok) {
    const err = await response.json().catch(() => ({}));
    const msg = err?.error?.message || err?.message || response.statusText;
    throw new Error(`${provider} API error (${response.status}): ${msg}`);
  }

  const data = await response.json();

  // Normalise response to plain string regardless of provider
  if (provider === "claude") {
    return data?.content?.[0]?.text ?? "";
  } else {
    // OpenAI-compatible shape (openai + openrouter + custom)
    return data?.choices?.[0]?.message?.content ?? "";
  }
}

/* ── AI-POWERED QUALIFICATION ────────────────────────────── */

async function classifyLeadAI(lead) {
  const prompt = `You are a senior marketing strategist qualifying inbound leads.

Lead details:
- Company: ${lead.company}
- Industry: ${lead.industry}
- Goal: ${lead.goal}
- Brand voice on file: ${lead.brandVoice}
- Approved offer: ${lead.offer}

Respond with ONLY valid JSON in this exact format (no explanation, no markdown):
{
  "score": <integer 0-96>,
  "urgency": "<High|Medium|Low>",
  "fit": "<one sentence: fit assessment>",
  "nextAction": "<one sentence: recommended next step>",
  "risk": "<one sentence: risk or governance note>"
}`;

  const raw = await callAI(prompt);
  if (!raw) return null; // simulation fallback

  try {
    const clean = raw.replace(/```json|```/g, "").trim();
    return JSON.parse(clean);
  } catch {
    throw new Error("Could not parse qualification response from AI. Raw response logged to console.");
  }
}

/* ── SIMULATION FALLBACK (original deterministic logic) ──── */

function classifyLeadSim(lead) {
  const goal = lead.goal.toLowerCase();
  let score = 52;
  if (goal.includes("conversion") || goal.includes("appointments") || goal.includes("leads")) score += 16;
  if (goal.includes("google ads") || goal.includes("paid") || goal.includes("campaign")) score += 12;
  if (goal.includes("wasting spend") || goal.includes("low") || goal.includes("urgent")) score += 10;
  if (lead.brandVoice.length > 40 && lead.offer.length > 40) score += 6;
  score = Math.min(score, 96);

  return {
    score,
    urgency:    score >= 82 ? "High" : score >= 68 ? "Medium" : "Low",
    fit:        score >= 80 ? "Strong fit" : score >= 65 ? "Qualified but needs discovery" : "Needs manual review",
    risk:       fields.humanReview.checked
                  ? "Controlled: external email remains draft until approval."
                  : "High: sending without review is not recommended in practice mode.",
    nextAction: score >= 75
                  ? "Book a diagnostic call and request current campaign metrics."
                  : "Ask clarifying questions before proposing a sprint.",
  };
}

