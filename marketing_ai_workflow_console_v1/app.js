/* ── MAIN WORKFLOW ───────────────────────────────────────── */

async function runWorkflow() {
  const lead = getLead();
  const missing = validateLead(lead);
  if (missing.length) {
    log(`<span class="risk">Blocked:</span> missing ${missing.join(", ")}.`);
    return;
  }

  const provider = fields.aiProvider.value;
  const isLive   = provider !== "simulation";

  setRunLoading(true);
  output.emailDraft.classList.add("generating");
  output.briefDraft.classList.add("generating");
  output.emailDraft.textContent = isLive ? "Generating with AI…" : "Running simulation…";
  output.briefDraft.textContent = isLive ? "Generating with AI…" : "Running simulation…";

  try {
    log("Lead intake captured from Gmail/Form simulation.");
    log("Project knowledge retrieved from Google Drive simulation.");

    // Step 2 — Qualification
    let analysis;
    if (isLive) {
      log("Sending qualification request to AI provider…");
      const aiAnalysis = await classifyLeadAI(lead);
      if (aiAnalysis) {
        // Merge governance fields that are local
        analysis = {
          ...aiAnalysis,
          risk: fields.humanReview.checked
            ? aiAnalysis.risk + " Human approval gate is active."
            : aiAnalysis.risk + " Warning: approval gate is disabled.",
        };
        log(`AI provider returned fit score ${analysis.score} — urgency: ${analysis.urgency}.`);
      } else {
        analysis = classifyLeadSim(lead);
        log("Simulation engine assigned fit score (AI path returned empty).");
      }
    } else {
      analysis = classifyLeadSim(lead);
      log(`Simulation engine assigned fit score ${analysis.score} and ${analysis.urgency.toLowerCase()} urgency.`);
    }
    renderQualification(analysis);

    // Step 5 — Email draft
    const email = await buildEmailDraft(lead, analysis);
    output.emailDraft.textContent = email;
    output.emailDraft.classList.remove("generating");
    log(isLive ? "Email draft generated by AI provider." : "Email draft generated by simulation engine.");

    // Step 6 — Creative brief
    const brief = await buildCreativeBrief(lead, analysis);
    output.briefDraft.textContent = brief;
    output.briefDraft.classList.remove("generating");
    if (fields.creativeBrief.checked) {
      log(isLive ? "Creative brief generated by AI provider." : "Simulation creative brief generated.");
    }

    // Step 7 — Tasks
    renderTask(lead, analysis);
    if (fields.createTask.checked) log("Notion-style follow-up tasks created.");

    // Governance gate
    if (fields.humanReview.checked) {
      log("Human approval gate active — external email remains draft until reviewed.");
    } else {
      log(`<span class="warning">Warning:</span> approval gate is disabled for this run.`);
    }

  } catch (err) {
    output.emailDraft.textContent = `Error: ${err.message}`;
    output.emailDraft.classList.remove("generating");
    output.briefDraft.textContent = "Workflow stopped due to error. See audit log.";
    output.briefDraft.classList.remove("generating");
    log(`<span class="risk">Error:</span> ${err.message}`);
    console.error(err);
  } finally {
    setRunLoading(false);
  }
}

